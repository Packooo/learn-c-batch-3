#include <iostream>
using namespace std;

int main(){
	//mendeklarasikan variable dan memberi nilai
	int A = 10;
	int B = 10;
	
	//melakukan increment
	A++; //sama dengan A ditambah 1
	
	//Melakukan decrement
	B--; //sama dengan B dikurang 1
	
	//menampilkan nilai A akhir
	cout<<"Nilai A akhir : "<<A <<endl;
	
	//menampilkan nilai B akhir
	cout<<"nilai B akhir : " <<B <<endl;
}